    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {                                           
        try {
            String nazivPostavke = txtNazivPostavke.getText();
            if (nazivPostavke.isEmpty()) {
                throw new Exception("Niste uneli naziv postavke");
            }
            String datumFormiranjaText = txtDatumFormiranja.getText();
            if (datumFormiranjaText.isEmpty()) {
                throw new Exception("Niste uneli datum formiranja");
            }
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumFormiranjaDate = sdf.parse(datumFormiranjaText);
            if(datumFormiranjaDate.before(new Date()) && formEnum.equals(FormEnum.NOVI))
                throw new Exception("Datum formiranja mora biti neki buduci datum");
            
            Kustos kustos=(Kustos) cmbKustos.getSelectedItem();
                
            stalnaPostavka.setNazivPostavke(nazivPostavke);
            stalnaPostavka.setDatumFormiranja(datumFormiranjaDate);
            stalnaPostavka.setKustos(kustos);
         
            if (formEnum.equals(FormEnum.NOVI)) {
                stalnaPostavka.setBrojEksponata(0);
                ClientController.getInstance().saveStalnaPostavka(stalnaPostavka);
                JOptionPane.showMessageDialog(this, "Uspesno ste sacuvali novu stalnu postavku");
            } else {
                ClientController.getInstance().editStalnaPostavka(stalnaPostavka);
                JOptionPane.showMessageDialog(this, "Uspesno ste izmenili stalnu postavku");
            }
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server vam je prekinuo komunikaciju! Gasenje programa...", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }                                          

