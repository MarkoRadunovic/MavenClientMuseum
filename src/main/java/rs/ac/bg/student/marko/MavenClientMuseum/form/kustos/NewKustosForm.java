    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {                                           
        try {
            String ime = txtIme.getText().trim();
            if (ime.isEmpty()) {
                throw new Exception("Niste uneli ime kustosa");
            }
            String prezime = txtPrezime.getText().trim();
            if (prezime.isEmpty()) {
                throw new Exception("Niste uneli prezime kustosa");
            }

            String adresa = txtAdresa.getText().trim();
            if (adresa.isEmpty()) {
                throw new Exception("Niste uneli adresu kustosa");
            }

            String godineText = txtGodine.getText().trim();
            if (godineText.isEmpty()) {
                throw new Exception("Niste uneli godine kustosa");
            }
            int godine = Integer.parseInt(godineText);
            if(godine<18)
                throw new Exception("Kustos mora biti punoletan");

            Specijalnost specijalnost = (Specijalnost) cmbSpecijalnost.getSelectedItem();

            kustos.setIme(ime);
            kustos.setAdresa(adresa);
            kustos.setGodine(godine);
            kustos.setPrezime(prezime);
            kustos.setSpecijalnost(specijalnost);
            if (formEnum.equals(FormEnum.NOVI)) {
                ClientController.getInstance().saveKustos(kustos);
                JOptionPane.showMessageDialog(this, "Uspesno ste sacuvali kustosa");
            } else {
                ClientController.getInstance().editKustos(kustos);
                JOptionPane.showMessageDialog(this, "Uspesno ste izmenili kustosa");
            }
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            if (ex instanceof SocketException) {
                JOptionPane.showMessageDialog(this, "Server je prekinuo komunikaciju", "Greska", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }                                          

